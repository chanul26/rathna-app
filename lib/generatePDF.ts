import jsPDF from 'jspdf'
import type { PassportFormData } from '@/components/PassportForm'
import type { GNFormData } from '@/components/GNForm'
import type { BusinessFormData } from '@/components/BusinessForm'

const PLACEHOLDER = '________________________'

function drawField(
  doc: jsPDF,
  label: string,
  value: string | undefined,
  yPos: number,
  valueX = 70,
): number {
  const pageWidth = doc.internal.pageSize.getWidth()
  const valueMaxWidth = pageWidth - valueX - 20

  doc.setFont('helvetica', 'bold')
  doc.text(`${label}:`, 20, yPos)

  doc.setFont('helvetica', 'normal')
  const display = value?.trim() ? value.trim() : PLACEHOLDER
  const lines = doc.splitTextToSize(display, valueMaxWidth)
  doc.text(lines, valueX, yPos)

  return yPos + 12 * Math.max(1, lines.length)
}

function drawSignatureFooter(doc: jsPDF, yPos: number) {
  doc.setLineWidth(0.2)
  doc.line(20, yPos, 80, yPos)
  doc.setFontSize(9)
  doc.setFont('helvetica', 'italic')
  doc.text("Applicant's Signature", 30, yPos + 5)
  doc.text('Generated by Rathna AI Assistant', 105, 280, { align: 'center' })
}

function printPdfDoc(doc: jsPDF) {
  const blob = doc.output('blob')
  const url = URL.createObjectURL(blob)

  const iframe = document.createElement('iframe')
  iframe.style.position = 'fixed'
  iframe.style.right = '0'
  iframe.style.bottom = '0'
  iframe.style.width = '0'
  iframe.style.height = '0'
  iframe.style.border = 'none'
  iframe.src = url

  const cleanup = () => {
    URL.revokeObjectURL(url)
    iframe.remove()
  }

  iframe.onload = () => {
    try {
      iframe.contentWindow?.focus()
      iframe.contentWindow?.print()
    } finally {
      window.setTimeout(cleanup, 1000)
    }
  }

  document.body.appendChild(iframe)
}

function buildPassportPDFDoc(data: PassportFormData) {
  const doc = new jsPDF({
    orientation: 'portrait',
    unit: 'mm',
    format: 'a4',
  })

  doc.setTextColor(0, 0, 0)
  doc.setFontSize(14)
  doc.setFont('helvetica', 'bold')
  doc.text('SRI LANKA DEPARTMENT OF IMMIGRATION & EMIGRATION', 105, 20, {
    align: 'center',
  })

  doc.setFontSize(16)
  doc.text('PASSPORT APPLICATION (FORM K)', 105, 30, { align: 'center' })
  doc.setLineWidth(0.5)
  doc.line(20, 35, 190, 35)

  doc.setFontSize(11)
  let yPos = 50

  yPos = drawField(doc, 'Surname', data.surname, yPos)
  yPos = drawField(doc, 'Other Names', data.otherNames, yPos)
  yPos = drawField(doc, 'NIC Number', data.nationalId, yPos)
  yPos = drawField(doc, 'Date of Birth', data.dateOfBirth, yPos)
  yPos = drawField(doc, 'Place of Birth', data.placeOfBirth, yPos)
  yPos = drawField(doc, 'Gender', data.gender, yPos)

  yPos += 5
  yPos = drawField(doc, 'Permanent Address', data.address, yPos)
  yPos = drawField(doc, 'District', data.district, yPos)
  yPos = drawField(doc, 'Phone Number', data.phone, yPos)
  yPos = drawField(doc, 'Service Type', data.serviceType, yPos)

  drawSignatureFooter(doc, yPos + 30)
  return doc
}

